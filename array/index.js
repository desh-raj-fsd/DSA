/**
 * An array in JavaScript is a data structure used to store multiple values in a single variable.
 * These values can be of any data type, including numbers, strings, objects, other arrays, or even a mix of these types.
 * Arrays are dynamic, meaning their size can change, and they are zero-indexed, meaning the first element is at index 0.
 *
 * JavaScript offers a wide range of array methods to perform operations like adding, removing, sorting, filtering, and transforming elements. As of now, JavaScript includes 37 built-in array methods, grouped by their functionality:
 *
 * Array Creation and Manipulation
 * Method	            Description
 * Array.isArray()	    Checks if a value is an array.
 * Array.from()	        Creates an array from an array-like or iterable object.
 * Array.of()	        Creates an array from a set of values.
 * concat()	            Combines two or more arrays and returns a new array.
 * copyWithin()	        Copies part of an array to another location within the same array.
 * fill()	            Fills all elements in an array with a static value.
 * push()	            Adds one or more elements to the end of an array and returns the new length.
 * pop()	            Removes the last element from an array and returns it.
 * shift()	            Removes the first element from an array and returns it.
 * unshift()	        Adds one or more elements to the beginning of an array and returns the new length.
 * splice()	            Adds, removes, or replaces elements within an array.
 * slice()	            Returns a shallow copy of a portion of an array into a new array.
 * reverse()	        Reverses the order of elements in an array in place.
 *
 * Searching and Filtering
 * Method	            Description
 * indexOf()	        Returns the first index of a specified value, or -1 if not found.
 * lastIndexOf()	    Returns the last index of a specified value, or -1 if not found.
 * includes()	        Checks if an array contains a specified value.
 * find()	            Returns the first element that satisfies a condition.
 * findIndex()	        Returns the index of the first element that satisfies a condition.
 * filter()	            Creates a new array with all elements that satisfy a condition.
 * every()	            Checks if all elements pass a test.
 * some()	            Checks if at least one element passes a test.
 *
 * Iteration and Transformation
 * Method	            Description
 * forEach()	        Executes a provided function once for each array element.
 * map()	            Creates a new array with the results of calling a provided function on every element.
 * reduce()	            Reduces the array to a single value by applying a function cumulatively.
 * reduceRight()	    Same as reduce(), but operates from right to left.
 * flat()	            Flattens nested arrays into a single array.
 * flatMap()	        Maps each element and flattens the result into a new array.
 *
 * Sorting
 * Method	            Description
 * sort()	            Sorts the elements of an array in place.
 * join()	            Joins all elements of an array into a string.
 * toLocaleString()	    Converts elements to strings using locale-specific conventions.
 * toString()	        Converts the array to a string.
 *
 * Other Utilities
 * Method	            Description
 * keys()	            Returns an iterator for the keys in the array.
 * values()	            Returns an iterator for the values in the array.
 * entries()	        Returns an iterator for key-value pairs in the array.
 * at()	                Returns the element at the specified index (supports negative indexing).
 * Array.prototype[@@iterator]()	Returns the default iterator for arrays (same as values()).
 *
 */

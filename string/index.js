/**
 * String Methods in JavaScript
 *
 * JavaScript strings are used for storing and manipulating text. Strings are zero-indexed, meaning the first character has an index of 0.
 * JavaScript provides many methods to perform operations on strings, such as searching, extracting, and manipulating string values.
 * Here is a summary of the commonly used string methods:
 *
 * String Creation and Manipulation
 * Method                Description
 * charAt()              Returns the character at a specified index.
 * charCodeAt()          Returns the Unicode of the character at a specified index.
 * concat()              Combines two or more strings and returns a new string.
 * includes()            Checks if a string contains a specified substring.
 * indexOf()             Returns the first index of a specified substring, or -1 if not found.
 * lastIndexOf()         Returns the last index of a specified substring, or -1 if not found.
 * match()               Searches a string for a match against a regular expression.
 * matchAll()            Returns an iterator of all matches of a regular expression.
 * replace()             Replaces a specified value with another value in a string.
 * replaceAll()          Replaces all occurrences of a specified value with another value in a string.
 * search()              Searches a string for a value matching a regular expression and returns the index of the match.
 * slice()               Extracts a part of a string and returns it as a new string.
 * split()               Splits a string into an array of substrings.
 * substring()           Extracts characters from a string, between two specified indices.
 * toLowerCase()         Converts a string to lowercase letters.
 * toUpperCase()         Converts a string to uppercase letters.
 * trim()                Removes whitespace from both ends of a string.
 * trimStart()           Removes whitespace from the start of a string.
 * trimEnd()             Removes whitespace from the end of a string.
 *
 * Searching and Matching
 * Method                Description
 * startsWith()          Checks if a string starts with a specified substring.
 * endsWith()            Checks if a string ends with a specified substring.
 * includes()            Checks if a string contains a specified substring.
 *
 * Iteration and Transformation
 * Method                Description
 * repeat()              Returns a new string with a specified number of copies of an existing string.
 * padStart()            Pads the beginning of a string with another string to a specified length.
 * padEnd()              Pads the end of a string with another string to a specified length.
 * toLowerCase()         Converts a string to lowercase letters.
 * toUpperCase()         Converts a string to uppercase letters.
 *
 * Template Literals
 * Method                Description
 * `string literals`     Allows embedded expressions and multi-line strings.
 *
 * Additional String Methods
 * Method                Description
 * codePointAt()         Returns a non-negative integer that is the Unicode code point value at the specified position.
 * fromCharCode()        Returns a string created from the specified sequence of UTF-16 code units.
 * fromCodePoint()       Returns a string created from the specified sequence of code points.
 * normalize()           Returns the Unicode Normalization Form of the string.
 * localeCompare()       Compares two strings in the current locale.
 * raw()                 Returns a string created from a raw template string, without processing escape sequences.
 * at()                  Returns the character at the specified index, allowing negative indices.
 */
